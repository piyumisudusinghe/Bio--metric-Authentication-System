    private boolean validate_login(String username,String password) {
       try{   
           Class.forName("com.mysql.jdbc.Driver");  // MySQL database connection
           Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/biometric_authentication?" + "user=root&password=123");     
           PreparedStatement pst = (PreparedStatement) conn.prepareStatement("Select user_id,password from login_details where user_name=?");
           pst.setString(1, username); 
           ResultSet rs = pst.executeQuery(); 
            if(rs.next()) { 
              String actualpwd = rs.getString("password");
              int id = rs.getInt("user_id");
              this.newuserid = id;
              if(actualpwd == null ? password != null : !actualpwd.equals(password)){
                return false; 
              }else{
                return true;
              }
            }else{
              return false;
            }
        }catch(Exception e){
            e.printStackTrace();
            return false;
        }
    }

