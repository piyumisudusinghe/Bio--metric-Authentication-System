    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if((txtuser_name.getText().length()==0 )||(txtpassword.getPassword().length==0)){
            JOptionPane.showMessageDialog(null,"Empty fields detected ! Please fill up all fields");   
        }else{
            String user = txtuser_name.getText();   // Collecting the input
            char[] pass = txtpassword.getPassword(); // Collecting the input
            String pwd = String.copyValueOf(pass);  // converting from array to string
            if(validate_login(user,pwd)){
                
                if(5<count){
                    
                    calculateThreshold.calculateDDKLThreshold();
                 
                    calculateThreshold.calculateDUKLThreshold();
                   
                    calculateThreshold.calculateKDThreshold();
                   
                    calculateThreshold.calculateUDKLThreshold();
                    
                    calculateThreshold.calculateUUKLThreshold();
                    
                    try {
                         double errorrate = calculateThreshold.compareThreshold(newuserid);
                         if(errorrate<3){
                            this.setVisible(false);
                            LoggedForm loggedForm = new LoggedForm(newuserid);
                            loggedForm.setVisible(true);
                         }else{
                            JOptionPane.showMessageDialog(null,"Invalid login. Please try again");
                            this.setVisible(false);
                            new HomeNew().setVisible(true);
                         }
                    } catch (ClassNotFoundException ex) {
                          Logger.getLogger(LoginNew.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (SQLException ex) {
                          Logger.getLogger(LoginNew.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                   keyAllocater.calculate_DDKL();
                   
                   keyAllocater.calculate_DUKL();
                  
                   keyAllocater.calculate_KD();
                  
                   keyAllocater.calculate_UDKL();
                   
                   keyAllocater.calculate_UUKL();
                   
                   this.setVisible(false);
                   LoginNew loginNew = new LoginNew(count+1);
                   loginNew.setVisible(true);
                   loginNew.txtuser_name.setText(user);
                   loginNew.txtpassword.requestFocus();
                   
                }
                
                
               
                
            }else{
                // this.setVisible(false);
                 JOptionPane.showMessageDialog(null,"Please enter correct password ");
                // new HomeNew().setVisible(true);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

